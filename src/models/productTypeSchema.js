const mongoose = require("mongoose");

const ProductType = new mongoose.Schema({
  typeName: {
    type: String,
    required: true,
  },
  status: {
    type: Boolean,
    default: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("ProductType", ProductType);
